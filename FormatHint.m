function FormatHint
%  创建一个“输入参数文件格式提示”对话框；
%  当用户点击确定时，返回index值1；
%  当用户点击取消时，返回index值0。

%  创建三个“空格数组”。
blank1=ones(1,20);
blank2=ones(1,16);
blank3=ones(1,22);
blank1=blank1*32;
blank2=blank2*32;
blank3=blank3*32;
%  设定普通对话框的部分Position参数。
ParentPos=get(gcbf,'Position');  %  gcbf表示正在执行的Callback对应的对象所在窗口的句柄，即handles.figure1的句柄
xoffset=ParentPos(1)+0.32982456*ParentPos(3);
yoffset=ParentPos(2)+0.2183908*ParentPos(4);
%  创建普通对话框。
hdialog=dialog('Name','输入提示','Units','pixels','Position',[xoffset yoffset 384 480]);
delete(get(hdialog,'Children'));
uicontrol('Style','text','Units','Normalized','Position',[0.1 0.17 0.8 0.8],...
          'FontWeight','bold','FontSize',10,'Parent',hdialog,...
          'HorizontalAlignment','left','String',...
          {'请选择一个写有仪器响应参数的文本文档，作为精确校正的输入文件；',...
           '',...
           '该文档的数据存放格式如下：',...
           '------------------------------------------',...
           ['nz',blank1,'零点个数'],...
           ['zeros(1:nz)',32,32,32,32,32,32,32,'零点的复数值(Rad/sec)'],...
           ['np',blank1,'极点个数'],...
           ['poles(1:np)',32,32,32,32,32,32,32,'极点的复数值(Rad/sec)'],...
           ['norm',blank2,'归一化因子'],...
           ['g',blank3,'增益'],...,
           '',...
           '例如“仪器响应参数示例.txt”内容如下:',...
           '-------------------------------------------------------------',...
           '3',...
           ['140.0',32,32,32,32,'0.0'],...
           ['0.0',32,32,32,32,'0.0'],...
           ['0.0',32,32,32,32,'0.0'],...
           '3',...
           ['-11.78e-3',32,32,32,32,'11.78e-3'],...
           ['-11.78e-3',32,32,32,32,'-11.78e-3'],...
           ['-48.40',32,32,32,32,'0.0'],...
            '-0.346',...
             '912.0'});
uicontrol('Units','Normalized','Position',[0.386 0.05 0.23 0.08],'FontSize',10,...
           'Parent',hdialog,'String','确定','Callback','delete(gcf);');     
movegui(hdialog);  %  确保对话框出现在屏幕上       
uiwait(hdialog);





